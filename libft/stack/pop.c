#include "stack.h"

t_list	*ft_pop(t_list **list)
{
	t_list *top;

	top = NULL;
	if (!ft_empty(*list))
	{
		top = *list;
		list = &(*list)->next;
		top->next = NULL;
	}
	return (top);
}
