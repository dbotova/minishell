#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int idx;

	idx = 0;
	if (s && *f)
	{
		while (*s)
		{
			f(idx, s);
			s++;
			idx++;
		}
	}
}
